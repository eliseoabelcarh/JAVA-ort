package ingrediente;
import materiaPrima.MateriaPrima;

public class Ingrediente {

	private int codigoMateriaPrima;
	private int cantNecesaria;
	
	
	
	public Ingrediente(int codigoMateriaPrima, int cantNecesaria) {
		super();
		this.codigoMateriaPrima = codigoMateriaPrima;
		this.cantNecesaria = cantNecesaria;
	}
	public int getCodigoMateriaPrima() {
		return codigoMateriaPrima;
	}
	public int getCantNecesaria() {
		return cantNecesaria;
	}



	
	
	
	
	
	
	
	
	
	
	
	
}
